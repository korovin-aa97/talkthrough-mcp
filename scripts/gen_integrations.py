def spliced_readme(text: str) -> str:
    """Return README text with the marked install region regenerated."""
    if README_BEGIN not in text or README_END not in text:
        raise SystemExit("README.md is missing the gen:install markers")
    head, _, rest = text.partition(README_BEGIN)
    _, _, tail = rest.partition(README_END)
    return head + README_BEGIN + "\n\n" + readme_install_region() + "\n" + README_END + tail


