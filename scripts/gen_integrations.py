def build_artifacts() -> dict[str, str]:
    artifacts: dict[str, str] = {}
    for builder in (
        build_claude_commands,
        build_example_prompts,
        build_skill_mirrors,
        build_agent_mirrors,
        build_mcp_configs,
        build_engine_docs,
        build_plugin_manifests,
        build_registry_manifest,
        build_clawhub_skill,
        build_integrations_index,
    ):
        artifacts.update(builder())
    return artifacts


def main() -> None:
    for rel_path, content in sorted(build_artifacts().items()):
        path = REPO / rel_path
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
        print(rel_path)
    readme = REPO / "README.md"
    readme.write_text(spliced_readme(readme.read_text(encoding="utf-8")), encoding="utf-8")
    print("README.md (install region)")


if __name__ == "__main__":
    main()
